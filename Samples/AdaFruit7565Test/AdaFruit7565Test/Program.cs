namespace AdaFruit7565Test {
    public class Program {
        //  B-: D5 (PWM) (LED Blue)
        //  G-: D6 (PWM) (LED Green)
        //  A+: 3.3v (LED anode)
        //  R-: D9 (PWM) (LED Red)

        //  GND: GND
        //  VDD: 3.3v

        //  SID: D11 (SPI MOSI)
        //  SCLK: D13 (SPI CLK)
        //  A0: D7 (Data / Command)
        //  /RST: D4
        //  /CS: D10 (SPI CS), D8 if D10 used for SD Card CS
        public static RGBLed rgbLed = new RGBLed(Pins.GPIO_PIN_D9, Pins.GPIO_PIN_D6, Pins.GPIO_PIN_D5);
        public static AdaFruit7565 lcd = new AdaFruit7565(Pins.GPIO_PIN_D7, Pins.GPIO_PIN_D4, Pins.GPIO_PIN_D10,speedKHz:21000);

        public const uint DefaultContrast = AdaFruit7565.ContrastMedium;

        public static void Main() {
            rgbLed.SetColor(RGBLed.Color.Black);
            lcd.Initialize();
            lcd.SetContrast(DefaultContrast);

            DisplayNyanCat();
            DisplayDarthVader();
            DisplayHelloKitty();
            DisplaySpaceInvader();

            rgbLed.SetColor(RGBLed.Color.White);

            DisplayCircles();
            DisplaySquares();

            rgbLed.SetColor(RGBLed.Color.Cyan);

            DisplayText();
            DisplayLines();

            rgbLed.SetColor(RGBLed.Color.Black);

            lcd.PowerSaveMode();
        }

        public static void DisplayNyanCat() {
            
            var frameCount = nyanCatFrames.Length / AdaFruit7565.bufferSize;
            
            var rainbow = new int[] {
                (int)RGBLed.Color.Red,
                (int)RGBLed.Color.Orange,
                (int)RGBLed.Color.Yellow,
                (int)RGBLed.Color.Green,
                (int)RGBLed.Color.Blue,
                (int)RGBLed.Color.Violet
            };

            var cycles = 10;

            while (cycles-- != 0) {
                for (var frame = 0; frame < frameCount; frame++) {
                    Array.Copy(nyanCatFrames, frame * AdaFruit7565.bufferSize, lcd.displayBuffer, 0, lcd.displayBuffer.Length);
                    lcd.Refresh();
                    rgbLed.SetColor((RGBLed.Color)rainbow[frame],165);
                    frame++;
                }
            }
        }

        public static void ContrastTest() {
            uint contrast = DefaultContrast;
            for (; contrast < 64; contrast++) {
                lcd.SetContrast(contrast);
                Thread.Sleep(40);
            }
            for (; contrast > 0; contrast--) {
                lcd.SetContrast(contrast);
                Thread.Sleep(40);
            }
            for (; contrast <= DefaultContrast; contrast++) {
                lcd.SetContrast(contrast);
                Thread.Sleep(40);
            }
            Thread.Sleep(500);
        }

        public static void DisplayDarthVader() {
            lcd.ClearScreen();
            Array.Copy(darthVader, lcd.displayBuffer, lcd.displayBuffer.Length);
            lcd.InvertDisplay(true);
            lcd.Refresh(); 
            rgbLed.SetColor(RGBLed.Color.Red);
            Thread.Sleep(2000);
            lcd.ClearScreen();
            lcd.Refresh();
            rgbLed.SetColor(RGBLed.Color.Black);
            lcd.InvertDisplay(false);
        }

        public static void DisplayHelloKitty() {
            lcd.ClearScreen();
