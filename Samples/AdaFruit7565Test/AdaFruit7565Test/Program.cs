            lcd.ClearScreen();
            lcd.Refresh();
            rgbLed.SetColor(RGBLed.Color.Black);
            lcd.InvertDisplay(false);
        }

        public static void DisplaySquares() {
            var y = AdaFruit7565.Height / 2;
            var dimension = 1;
            for (; dimension < 32; dimension++) {
                lcd.ClearScreen();
                var x = 32;
                lcd.DrawRectangle(x - (dimension / 2), y - (dimension / 2), dimension, dimension, AdaFruit7565.Color.White);
                x += 32;
                lcd.DrawRectangle(x - (dimension / 2), y - (dimension / 2), dimension, dimension, AdaFruit7565.Color.White);
                x += 32;
                lcd.DrawRectangle(x - (dimension / 2), y - (dimension / 2), dimension, dimension, AdaFruit7565.Color.White);
                lcd.Refresh();
            }
            for (; dimension > 0; dimension--) {
                lcd.ClearScreen();
                var x = 32;
                lcd.DrawRectangle(x - (dimension / 2), y - (dimension / 2), dimension, dimension, AdaFruit7565.Color.White);
                x += 32;
                lcd.DrawRectangle(x - (dimension / 2), y - (dimension / 2), dimension, dimension, AdaFruit7565.Color.White);
                x += 32;
                lcd.DrawRectangle(x - (dimension / 2), y - (dimension / 2), dimension, dimension, AdaFruit7565.Color.White);
                lcd.Refresh();
            }
        }

        public static void DisplayCircles() {
            var y = AdaFruit7565.Height / 2;
            var x = 0;
            var r = 1;
            for (; r < 31; r++) {
                lcd.ClearScreen();
                lcd.DrawCircle(x + 32, y, r, AdaFruit7565.Color.White);
                lcd.DrawCircle(x + 64, y, r, AdaFruit7565.Color.White);
                lcd.DrawCircle(x + 96, y, r, AdaFruit7565.Color.White);
                lcd.Refresh();
            }
            for (; r > 0; r--) {
                lcd.ClearScreen();
                lcd.DrawCircle(x + 32, y, r, AdaFruit7565.Color.White);
                lcd.DrawCircle(x + 64, y, r, AdaFruit7565.Color.White);
                lcd.DrawCircle(x + 96, y, r, AdaFruit7565.Color.White);
                lcd.Refresh();
            }
        }

        public static void DisplayLines() {
            lcd.AutoRefreshScreen = true;
            var x1 = 0;
            for (; x1 < AdaFruit7565.Width; x1++) {
                lcd.DrawLine(0, AdaFruit7565.Height - 1, x1, 0, AdaFruit7565.Color.White);
            }
            for (; x1 > 0; x1--) {
                lcd.DrawLine(AdaFruit7565.Width - 1, 0, x1, AdaFruit7565.Height - 1, AdaFruit7565.Color.White);
            }
            lcd.AutoRefreshScreen = false;
            Thread.Sleep(1000);
        }

        public static void DisplayText() {
            lcd.ClearScreen();

            int line = 1;

            lcd.DrawString(0, line++, "~~~~~~~~~~~~~~~~~~~~~~");
            lcd.DrawString(0, line++, " adafruit | netduino  ");
            lcd.DrawString(0, line++, " ST7565 display driver");
            lcd.DrawString(0, line++, "   netduino helpers   ");
            lcd.DrawString(0, line++, "        library       ");
            lcd.DrawString(0, line++, "  http://codeplex.com ");
            lcd.DrawString(0, line++, "~~~~~~~~~~~~~~~~~~~~~~");
            lcd.Refresh();

            Thread.Sleep(2000);
        }

        public static void BlinkTest() {
            for (var I = 0; I < 6; I++) {
                lcd.InvertDisplay(true);
                Thread.Sleep(100);
                lcd.InvertDisplay(false);
                Thread.Sleep(100);
            }
        }

